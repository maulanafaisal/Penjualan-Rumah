    private void BProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BProsesActionPerformed
        RandomID();
        jNopelanggan.setText(""+no_pelanggan);
        txtSetuju.setEnabled(true);
        Ltersedia = Integer.parseInt(txtLuasTersedia.getText());
        Htanah = Integer.parseInt(txtHargaTanah.getText());
        Hbangunan = Integer.parseInt(txtHargaBangunan.getText());
        int tanah = Htanah*Ltersedia;
        int tot = tanah+Hbangunan;
        txtHarga.setText(""+tot);
        int nPpn = tot/100*10;
        txtPpn.setText(""+nPpn);
        nDp = Integer.parseInt(txtDp.getText());
        int bayar = tot+nPpn-nDp;
        nPeriode = Integer.parseInt(txtPeriode.getText());
        int nBulan = nPeriode;
        int nCicilan = bayar/nBulan;
        txtCicilan.setText(""+nCicilan);
    }//GEN-LAST:event_BProsesActionPerformed

