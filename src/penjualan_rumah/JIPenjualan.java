    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO "
                    +"tabeldata(nama, area, harga_tanah, harga_bangunan, "
                    +"tipe_rumah, luas_tanah_a, luas_tanah_t, harga, dp, periode ,ppn, cicilan, no_pelanggan)"
                    +"values(?,?,?,?,?,?,?,?,?,?,?,?,?)");
            
            stmt.setString(1, txtNama.getText());
            stmt.setString(2, area);
            stmt.setString(3, txtHargaTanah.getText());
            stmt.setString(4, txtHargaBangunan.getText());
            stmt.setString(5, tipe);
            stmt.setString(6, txtLuasAsli.getText());
            stmt.setString(7, txtLuasTersedia.getText());
            stmt.setString(8, txtHarga.getText());
            stmt.setString(9, txtDp.getText());
            stmt.setString(10, txtPeriode.getText());
            stmt.setString(11, txtPpn.getText());
            stmt.setString(12, txtCicilan.getText());
            stmt.setString(13, no_pelanggan);

            stmt.executeUpdate();
            no_pelanggan = "";
            jNopelanggan.setText("");
            txtNama.setText("");
            cmbArea.setSelectedIndex(0);
            txtHargaTanah.setText("");
            txtHargaBangunan.setText("");
            RBTipe1.setSelected(false);
            RBTipe24.setSelected(false);
            RBTipe30.setSelected(false);
            txtLuasAsli.setText("");
            txtLuasTersedia.setText("");
            txtHarga.setText("");
            txtDp.setText("");
            txtPeriode.setText("");
            txtPpn.setText("");
            txtCicilan.setText("");
            txtSetuju.setSelected(false);
            txtSetuju.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Berhasil!");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e);
            System.out.print(e);
        }
    }//GEN-LAST:event_jSimpanActionPerformed

